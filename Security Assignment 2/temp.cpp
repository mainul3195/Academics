#include <bits/stdc++.h>
using namespace std;
long long bigMod(long long b, long long p, long long m)
{
    if (p == 0)
        return 1;
    long long temp = bigMod(b, p / 2, m);
    temp = temp * temp % m;
    if (p & 1)
        temp = temp * b % m;
    return temp;
}
void solve()
{
    srand(static_cast<unsigned>(time(nullptr)));
    long long a, b, c;
    while (cin >> a >> b >> c)
        cout << bigMod(a, b, c) << "\n";
    return;
}
int32_t main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0);
    solve();
    return 0;
}
